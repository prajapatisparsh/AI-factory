"""
Pydantic schemas for the AI Factory system.
These enforce strict validation across all agent outputs.
"""

from typing import List, Dict, Optional, Any
from pydantic import BaseModel, Field, field_validator
from enum import Enum


class ProjectType(str, Enum):
    WEB_APP = "web_app"
    MOBILE_APP = "mobile_app"
    API = "api"
    DESKTOP = "desktop"
    OTHER = "other"


class Priority(str, Enum):
    CRITICAL = "Critical"
    HIGH = "High"
    MEDIUM = "Medium"
    LOW = "Low"


class EvaluationStatus(str, Enum):
    APPROVED = "APPROVED"
    REJECTED = "REJECTED"


class DocumentAnalysis(BaseModel):
    """Output from VisionAgent parsing uploaded documents."""
    project_type: ProjectType = Field(default=ProjectType.OTHER, description="Type of project being built")
    features: List[str] = Field(default_factory=list, max_length=25, description="List of key features (max 25)")
    personas: List[str] = Field(default_factory=list, max_length=5, description="User personas (max 5)")
    tech_hints: List[str] = Field(default_factory=list, description="Technology hints from document")
    ambiguities: List[str] = Field(default_factory=list, description="Unclear or ambiguous requirements")
    full_text: str = Field(default="", description="Full extracted text from document")
    
    @field_validator('features')
    @classmethod
    def limit_features(cls, v: List[str]) -> List[str]:
        return v[:25] if len(v) > 25 else v
    
    @field_validator('personas')
    @classmethod
    def limit_personas(cls, v: List[str]) -> List[str]:
        return v[:5] if len(v) > 5 else v


class UserStory(BaseModel):
    """Individual user story generated by PM Agent."""
    id: str = Field(..., pattern=r"^US-\d{3}$", description="Story ID in format US-001")
    title: str = Field(..., min_length=5, max_length=200, description="Brief title for the story")
    user_role: str = Field(..., description="The user persona/role")
    action: str = Field(..., description="What the user wants to do")
    benefit: str = Field(..., description="Why the user wants to do this")
    acceptance_criteria: List[str] = Field(..., min_length=2, max_length=5, description="2-5 acceptance criteria")
    priority: Priority = Field(default=Priority.MEDIUM, description="Story priority")
    
    @field_validator('acceptance_criteria')
    @classmethod
    def validate_criteria_count(cls, v: List[str]) -> List[str]:
        if len(v) < 2:
            raise ValueError("Must have at least 2 acceptance criteria")
        return v[:5] if len(v) > 5 else v


class UserStoriesCollection(BaseModel):
    """Collection of user stories."""
    stories: List[UserStory] = Field(..., min_length=5, max_length=20)


class ScoreBreakdown(BaseModel):
    """Breakdown of PM evaluation scores."""
    requirements: int = Field(..., ge=0, le=30, description="Requirements coverage (0-30)")
    architecture: int = Field(..., ge=0, le=25, description="Architecture soundness (0-25)")
    completeness: int = Field(..., ge=0, le=20, description="Specification completeness (0-20)")
    qa_compliance: int = Field(..., ge=0, le=15, description="QA issue resolution (0-15)")
    security: int = Field(..., ge=0, le=10, description="Security considerations (0-10)")


class PMEvaluation(BaseModel):
    """PM Agent's evaluation of generated specifications."""
    score: int = Field(..., ge=0, le=100, description="Overall score 0-100")
    status: EvaluationStatus = Field(..., description="APPROVED or REJECTED")
    breakdown: ScoreBreakdown = Field(..., description="Score breakdown by category")
    strengths: List[str] = Field(default_factory=list, description="What was done well")
    issues: List[str] = Field(default_factory=list, description="Issues found (if rejected)")
    scolding: str = Field(default="", description="Detailed feedback for rejected specs")
    
    @field_validator('issues')
    @classmethod
    def validate_issues_for_rejection(cls, v: List[str], info) -> List[str]:
        # Issues should be present if status is REJECTED
        return v
    
    @field_validator('scolding')
    @classmethod
    def validate_scolding_for_rejection(cls, v: str, info) -> str:
        # Scolding should be present if status is REJECTED
        return v


