class LearnedRule(BaseModel):
    """A rule learned by the Coach agent."""
    date: str = Field(..., description="Date in YYYY-MM-DD format")
    rule: str = Field(..., max_length=150, description="The learned rule (max 150 chars)")
    target_playbook: str = Field(..., description="Which playbook this applies to")
    
    @field_validator('rule')
    @classmethod
    def validate_rule_format(cls, v: str) -> str:
        # Rules should be actionable
        action_words = ['Never', 'Always', 'Ensure', 'Check', 'Verify', 'Include', 'Avoid']
        if not any(word in v for word in action_words):
            # Don't raise error, just log warning - we'll accept it anyway
            pass
        return v[:150] if len(v) > 150 else v


